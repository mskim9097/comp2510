#include <stdio.h>

int main() {
    int z[][3] = {{6, 5, 8}, {10, 6, 3}, {5, 7, 1}};
    printf("%d\n", z[1][2]); // 3

    int x[4][2] = {{4, 7}, {2, 8}, {8, 4}, {9, 0}};
    printf("%d\n", x[0][0]); // 4

    int i[][4] = {{0, 1, 5, 7}, {3, 8, 6, 9}};
    int j = i[0][1] * i[1][3];
    printf("%d", j); // 1 * 9 = 9

    return 0;
}